# 作業三：文字檔解析 - 手寫練習版
# 目標：仿照 UNIX 中 wc 公用程式解析文字檔
# 
# 練習要求：
# 1. 讀取 word_count.txt 檔案
# 2. 計算檔案中的行數、字數、字元數（不含標點符號）
# 3. 輸出結果

# ========== 第一步：讀取檔案 ==========
# 提示：使用 open() 開啟檔案，read().split("\n") 讀取並分割成行
# 請在下方寫程式碼：
infile =open('word_count.txt')

lines = infile.read().split("\n")
# 關閉檔案（良好的程式習慣）
infile.close()




# ========== 第二步：初始化計數器 ==========
# 提示：需要三個計數器 - 行數、字數、字元數
# 請在下方寫程式碼：
line_count = 0  # 行數計數器
word_count = 0  # 字數計數器
char_count = 0  # 字元計數器



# ========== 第三步：定義標點符號 ==========
# 提示：定義一個字串包含所有要排除的標點符號
# 請在下方寫程式碼：
punctuation = ".,!?;:\"'()[]{}/-"



# ========== 第四步：逐行處理文字 ==========
# 提示：使用 for 迴圈處理每一行
# 需要做三件事：
# 1. 計算非空行數量（使用 strip() 檢查）
# 2. 計算字數（使用 split() 分割單字）
# 3. 計算字元數（排除空白和標點符號）
# 請在下方寫程式碼：
for line in lines:
    # 1. 計算非空行數量
    if line.strip():  # 如果移除前後空白後還有內容，就不是空行
        line_count += 1
    
    # 2. 計算字數
    words = line.split()  # 使用 split() 分割成單字列表
    word_count += len(words)  # 加上這一行的單字數量
    
    # 3. 計算字元數（不含標點符號）
    for char in line:
        # 如果字元不是空白且不是標點符號，就計算
        if char != '' and char not in punctuation:
            # 累加字元計數
            char_count += 1




# ========== 第五步：輸出結果 ==========
# 提示：使用 f-string 格式化輸出
# 請在下方寫程式碼：
print(f"檔案中共有 {line_count} 行，{word_count} 個字，{char_count} 個字元(不含標點符號)")




