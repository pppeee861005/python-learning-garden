# loop, list & zip 迴圈與串列應用
#
# [練習三]
# 以天干、地支來排甲子
# 已知：
#   天干(tiangan)：'甲'~'癸' 共 10 個元素
#   地支(dizhi)：'子'~'亥' 共 12 個元素
# 輸出：
#   1) 天干與地支配對排列，即：'甲子'、'乙丑' .. '癸酉'
#   2) 然後是：'甲戌'、'乙亥'、'丙子' .. 接續排列
#   3) 排列至 '癸亥'(下個'甲子'之前)為止，並印出結果(共 60 個配對)
# (提示)：可用 zip() 或取餘數運算

tiangan = ['甲', '乙', '丙', '丁', '戊', '己', '庚', '辛', '壬', '癸']
dizhi = ['子', '丑', '寅', '卯', '辰', '巳', '午', '未', '申', '酉', '戌', '亥']

pairs = []
for i in range(60):
    pair = tiangan[i % 10] + dizhi[i % 12]
    pairs.append(pair)

print("六十甲子配對：")
for idx, pair in enumerate(pairs, 1):
    print(f"{idx:2d}: {pair}")
